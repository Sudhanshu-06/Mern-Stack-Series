// To store values we can use variables
// variables are memory bucket that stores our values and has a name as well.

// How to create variable in js
// -3 ways to create variable
// 1)- using var keyword

var marks=90;
var score=5;
console.log(marks,score);

// 2)- using let keyword

let age=25;
let flag=0;
console.log(age,flag);

// 3)- using const keyword

const pi=3.14;
console.log(pi);